export function translateText(value: string, language: AppLanguage) {
  if (language === 'zh-CN' || !containsChinese(value)) return value
  const cached = translatedTextCache.get(value)
  if (cached) return cached

  const leading = value.match(/^\s*/)?.[0] ?? ''
  const trailing = value.match(/\s*$/)?.[0] ?? ''
  const source = value.trim()
  const normalized = source.replace(/\s+/g, ' ')
  let translated = englishText[source] ?? englishText[normalized] ?? applyPatternRules(source)

  if (!translated) translated = translateComposite(source, language)

  if (!translated && source.includes(' · ')) {
    const parts = source.split(' · ').map((part) => translateText(part, language))
    translated = parts.join(' · ')
  }

  const result = translated ? `${leading}${translated}${trailing}` : value
  translatedTextCache.set(value, result)
  return result
}

function applyPatternRules(source: string) {
  for (const rule of patternRules) {
    const match = source.match(rule.pattern)
    if (match) return rule.replace(...match)
  }
  return ''
}

function translateComposite(source: string, language: AppLanguage) {
  const bilingual = source
    .replace(/[\u3400-\u9fffA-Z0-9\s·、]+[（(]([A-Za-z][^)）]+)[)）]/g, '$1')
    .replace(/：/g, ': ')
    .replace(/，/g, ', ')
    .replace(/；/g, '; ')
    .replace(/\s+/g, ' ')
    .trim()
  if (bilingual && bilingual !== source && !containsChinese(bilingual)) return bilingual

  const colonIndex = source.indexOf('：')
  if (colonIndex > 0) {
    const left = translateText(source.slice(0, colonIndex), language)
    const right = source.slice(colonIndex + 1).trim()
    const translatedRight = containsChinese(right) ? translateText(right, language) : right
    if (!containsChinese(left) && !containsChinese(translatedRight)) {
      return translatedRight ? `${left}: ${translatedRight}` : `${left}:`
    }
  }

  for (const separator of [' / ', '、', '，', '；']) {
    if (!source.includes(separator)) continue
    const joiner = separator === ' / ' ? ' / ' : separator === '、' ? ', ' : separator === '，' ? ', ' : '; '
    const parts = source.split(separator).map((part) => translateText(part, language))
    if (parts.every((part) => !containsChinese(part))) return parts.join(joiner)
  }

  return ''
}

export function translateMultiline(value: string, language: AppLanguage) {
  if (language === 'zh-CN') return value
  return value
    .split('\n')
    .map((line) => translateText(line, language))
    .join('\n')
}

function containsChinese(value: string) {
  return /[\u3400-\u9fff]/.test(value)
}
